def get_datasets_table():
    table = Table(box=box.MARKDOWN)
    table.add_column("Dataset Identifier")
    table.add_column("#Users")
    table.add_column("#Items")
    table.add_column("#Interactions")
    table.add_column("Feedback Type")

    for name, stats in _datasets.items():
        values = [
            name,
            stats["users"],
            stats["items"],
            stats["interactions"],
            stats["type"],
        ]
        values = [str(x) for x in values]
        table.add_row(*values)

    buf = StringIO()
    console = Console(file=buf, width=100, force_terminal=False)
    console.print(table)

    return buf.getvalue()
